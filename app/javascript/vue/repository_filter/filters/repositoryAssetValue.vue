<template>
  <div class="filter-attributes">
    <DropdownSelector
      :disableSearch="true"
      :options="operators"
      :selectorId="`OperatorSelector${filter.id}`"
      @dropdown:changed="updateOperator"
    />
    <div class="sci-input-container">
      <input v-if="operator === 'file_contains'"
        class="sci-input-field"
        type="text"
        name="value"
        v-model="value"
        :placeholder="i18n.t('repositories.show.repository_filter.enter_text')"
        @input="updateFilter"
      />
    </div>
  </div>
</template>

<script>
  import FilterMixin from 'vue/repository_filter/mixins/filter.js'
  import DropdownSelector from 'vue/shared/dropdown_selector.vue'

  export default {
    name: 'RepositoryAssetValue',
    mixins: [FilterMixin],
    props: {
      filter: Object
    },
    data() {
      return {
        operators: [
          { value: 'file_contains', label: this.i18n.t('repositories.show.repository_filter.filters.types.RepositoryAssetValue.operators.file_contains') },
          { value: 'file_attached', label: this.i18n.t('repositories.show.repository_filter.filters.types.RepositoryAssetValue.operators.file_attached') },
          { value: 'file_not_attached', label: this.i18n.t('repositories.show.repository_filter.filters.types.RepositoryAssetValue.operators.file_not_attached') }
        ],
        operator: 'file_contains',
        value: ''
      }
    },
    watch: {
      operator() {
        if(this.operator !== 'file_contains') this.value = '';
      }
    },
    components: {
      DropdownSelector
    },
    computed: {
      isBlank(){
        return this.operator == 'file_contains' && !this.value;
      }
    }
  }
</script>
